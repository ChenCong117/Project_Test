/*
some fix address description: for both RevA and RevB...

Fix-address is mainly for some flags, such as firmware is running, module work success, and so on...

0x0000 - 0x0003: main.c
0x0004 - 0x0007: sched.c & event.c
0x0008 - 0x001F: usbpd module
	0x08: event num
	0x09: not used
	0x0A: snk timeout bits(not used)
	0x0B: src timeout bits
	0x0C: hardrst rcv times
	0x0D: hardrst snd times
	0x0E: txretry times
	0x0F: discard times
	0x10 - 0x1B: not used
	0x1C: port state
	0x1D: connect status
	0x1E: not used
	0x1F: not used
*/


